<h3>Easy structure builder</h3>
<p>Codeigniter-Boilerplate helps you creating project structure thanks to a<br>
   simple template system based on a main container and single page content.<br>
   Your main template is build on HTML5 Boilerplate to guarante the best <br>
   crossborwser integration and easy CSS styling.
</p>

<h3>Technical notes</h3>
<ul>
    <li>Codeigniter Version: 2.0.1</li>
    <li>JQuery Version: 1.6</li>
    <li>HTML5 BP Version: 1.0</li>
</ul>

<h3>CI Configurations</h3>
<p>The basic Codeigniter configuration to make the Boilerplate works correctly is:</p>
<ul>
    <li>Autoload: url helper</li>
    <li>Routes: default_controller = "homepage"</li>
    <li>Index.php: subclass_prefix = "BP_"</li>
</ul>
