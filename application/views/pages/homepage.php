<h2>Codeigniter-Boilerplate</h2>
<h3>Easy structure builder</h3>

<ul>

    <li>
   Codeigniter-Boilerplate helps you start immediately a new project thanks to a<br>
   simple template based on <a href="http://html5boilerplate.com/" target ="_blank">HTML5 Boilerplate</a>. This guarante the best <br>
   crossbrowser experience and fast/easy CSS styling.</li>

   <li>
   Codeigniter-Boilerplate has a prebuild structure which encapsulates single page <br>
   views into a container view which shares parts of code as header, footer and script.<br>
   </li>

   <li>
   Codeigniter-Boilerplate has the primarly aim to help developer building simple <br>
   pages in the shortest time but without rigid structures, that means: allowing <br>
   definition of custom elements for any pages without resctrictions. <br>
   Developer as fast access to title, description, javascript, css, Google Font<br>
   and (obviously) content for any single page.
   </li>

</ul>


<h3>Technical notes</h3>
<ul>
    <li>Codeigniter Version: 2.0.2</li>
    <li>JQuery Version: 1.5.1 (waiting for HTML5 BP update)</li>
    <li>HTML5 BP Version: 1.0 Stripped</li>
</ul>

<h3>CI Configurations</h3>
<p>Codeigniter configuration for Boilerplate starts with:</p>
<ul>
    <li>Autoload: url helper</li>
    <li>Routes: default_controller = "homepage"</li>
    <li>Index.php: subclass_prefix = "BP_"</li>
    <li>Base url: http://localhost/codeigniter-boilerplate</li>
</ul>
